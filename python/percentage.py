# WAP that accepts the marks of 5 subjects and finds the sum and percentage marks obtained by the student


a=int(input("Enter the marks of subject 1:"))
b=int(input("Enter the marks of subject 2:"))
c=int(input("Enter the marks of subject 3:"))
d=int(input("Enter the marks of subject 4:"))
e=int(input("Enter the marks of subject 5:"))
sum=a+b+c+d+e
percentage=sum/5
print("The sum of 5 subject marks is:",sum)
print("The percentage of 5 subject marks is:",percentage)
